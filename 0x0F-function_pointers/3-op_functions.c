#include "3-cal.h"
#include <sttdio.h>
/**
 * op_add - a function that adds two intrgers
 * @a: one integer
 * @b: the second integer
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that subtrctd two integers
 * @a: one int
 * @b: the other int
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that multiplies two integers
 * @a: one integer
 * @b: the next integer
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that divides two integers
 * a: the first integer
 * b: the second integer
 * Return: the dividion result
 */
int op_div(int a, int b)
{
	return(a / b);
}
/**
 * op_mod - a function that finds the modulo
 * @a: the first int
 * @b: the second int
 * Return: the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}

